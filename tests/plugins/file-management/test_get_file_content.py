PROJECT_ROOT = Path(__file__).resolve().parents[3]


def _resolve_source_root():
    candidates = [
        # (PROJECT_ROOT / "usr" / "share" / "ahenk", "usr/share/ahenk"),
        (PROJECT_ROOT / "src", "src"),
        (PROJECT_ROOT, "root"),
    ]

    for candidate_path, candidate_name in candidates:
        plugin_path = candidate_path / "plugins" / "file-management" / "get_file_content.py"
        if plugin_path.exists():
            return candidate_path

    raise RuntimeError(
        "Unable to locate plugin source directory. "
        f"Checked: {[str(path) for path, _ in candidates]}"
    )


SOURCE_ROOT = _resolve_source_root()

if str(SOURCE_ROOT) not in sys.path:
    sys.path.insert(0, str(SOURCE_ROOT))

from base.model.enum.content_type import ContentType  # noqa: E402
